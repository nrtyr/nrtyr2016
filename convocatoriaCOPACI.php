					DE LA JORNADA ELECTORAL
					</h3>
					<br>
					<p class="pJustifi">
					<b>DÉCIMA.- </b>El día de la elección, no podrá realizarse proselitismo a favor de ninguna Planilla y Formula o candidato. La Jornada Electoral para la renovación de los Consejos de Participación Ciudadana y Delegaciones Municipales, tendrá verificativo el día domingo veintisiete de marzo  del dos mil dieciséis, instalándose la casilla a las 08:00 horas y realizándose la votación en un horario de   09:00 a 16:00 horas, la elección será mediante votación directa, sujetándose al procedimiento que a continuación se describe:
					<br>
					<br>

					Los Representantes del H. Ayuntamiento asignados por la Secretaria del H.  Ayuntamiento como Presidente, Secretario, Vocal uno y Vocal dos de la Casilla, procederán a la instalación de la misma, en presencia de los Representantes acreditados por las Planillas y Formulas participantes que estén presentes, efectuando las siguientes actividades:
					<br>
					<br>

					I.   En el caso de que existiera una sola Planilla o Formula registrada el día de la jornada electoral, los Representantes del H. Ayuntamiento convocarán a los vecinos y en presencia del Representante de la Planilla y Formula y de los integrantes de la misma, levantaran un Acta Circunstanciada en el formato que para ello les proporcione la Secretaria del H. Ayuntamiento;
					<br>
					<br>

					II.  Llamarán a acercarse a la Mesa a los Representantes de las Planillas y Formulas; 
					<br>
					<br>

					III. Mostrarán que la urna para recibir la votación se encuentre vacía;
					<br>
					<br>

					IV. Los Representantes del H. Ayuntamiento contarán las boletas asignadas a la Casilla, en presencia de los Representantes de las Planillas y Formula y anotarán la cantidad en el acta correspondiente;
					<br>
					<br>

					V. Levantarán el Acta de inicio de la Jornada Electoral Vecinal, anotando la hora y el nombre de los Representantes de las Planillas y Formulas presentes, recabando en este acto la firma de los mismos en la instalación; y 
					<br>
					<br>

					VI. Anunciarán el inicio de la votación, informando a los Representantes de las Planillas y Formulas que el horario para la recepción de la votación,  será hasta las 16:00 horas.
					<br>
					<br>

					<b>DÉCIMA PRIMERA.- </b>La votación será emitida  en el orden en que los vecinos se presenten ante la Casilla debiendo, para tal efecto, exhibir su credencial para votar con fotografía, actualizada y vigente; El voto solo podrá ser emitido para la localidad que se encuentre señalada en su credencial, ejercerán su voto de manera universal, libre, secreta, intransferible y directa. Si al llegar la hora señalada para el cierre de la jornada, la afluencia de votantes continuara, solo podrán votar los vecinos que antes del cumplimiento de la hora establecida estuviesen formados para emitir su votación.
					<br>
					<br>

					A los votantes se les entregará la Boleta Electoral que corresponda a la Elección del Consejo de Participación Ciudadana y una más para Delegaciones Municipales en donde esto aplique, previo registro de sus datos generales en el  Formato de Registro de Votantes de la Jornada Electoral.
					<br>
					<br>
					Después de sufragar su voto, se les colocará en el pulgar derecho tinta indeleble que acredite su sufragio.
					<br>
					<br>
					<b>DÉCIMA SEGUNDA.- </b>Los votos serán validos siempre y cuando se marque un solo número que corresponda a la  Planilla y Formula en la Boleta Electoral. En caso que aparezca anotado y marcado más de un número, el voto será declarado nulo. 
					<br>
					<br>


					<b>DÉCIMA TERCERA.- </b>Si durante la jornada se registraran actos que alteren la estabilidad y el orden del proceso, el Representante del H. Ayuntamiento que funja como Presidente de la Casilla, podrá suspender momentáneamente o en forma definitiva la elección, notificando inmediatamente a las autoridades correspondientes, asentándolo en el Acta de Cierre, Escrutinio y Computo de la Jornada Electoral Vecinal y elaborando una nota informativa que será entregada al Secretario del H. Ayuntamiento. 
					<br>
					<br>

					<b>DÉCIMA CUARTA.- </b>A las 16:00 horas del día de la elección y si no hubiera más votantes en espera, se procederá  a clausurar la Jornada Electoral Vecinal y se iniciará con el escrutinio y cómputo,  para tal efecto se levantará el acta correspondiente que contendrá los pormenores existentes durante este procedimiento y en donde se anotará el total de votos emitidos durante la jornada, los votos emitidos para cada una de las Planillas, la cantidad de boletas sobrantes inutilizadas así como los votos nulos, esta acta deberá de ser firmada por los Representantes del H. Ayuntamiento y los Representantes de las Planillas y Formulas.
					<br>
					<br>
					 
					Al término de la integración del paquete electoral, se publicará afuera del recinto donde se llevó a cabo la votación, una sábana que contendrá los resultados de la elección, la que deberá de estar firmada por los Representantes del H. Ayuntamiento y los Representantes de las Planillas y Formulas.
					<br>
					<br>

					Los Representantes del H. Ayuntamiento, serán los responsables de la entrega del paquete del proceso de elección, en las oficinas de la Secretaria del H. Ayuntamiento.
					<br>
					<br>

					<b>DÉCIMA QUINTA.- </b>A partir de la publicación de la presente Convocatoria, se suspenderán los programas asistenciales del Gobierno Municipal con las acepciones señaladas por la Legislación Electoral Estatal, a efecto de que no se utilicen recursos públicos a favor de alguna Planilla o Formula.
