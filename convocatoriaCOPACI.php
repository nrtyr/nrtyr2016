					<p class="pJustifi">
					<h3>
					CONVOCATORIA PARA LA ELECCIÓN DE LOS CONSEJOS <br>
					DE PARTICIPACIÓN CIUDADANA Y DELEGACIONES MUNICIPALES <br>
					PARA EL PERIODO 2016-2019 
					</h3>
					</p>
					<p class="pJustifi">
					Dirigida a todos los vecinos de las Colonias, Fraccionamientos y Pueblos citados en la presente convocatoria en vigor,  que deseen participar en el proceso democrático de la renovación de los Consejos de Participación Ciudadana y Delegaciones de la localidad donde tengan su residencia legal, de acuerdo a las siguientes:
					</p>
					<br>
					<br>
					<h3> B A S E S </h3>
					<h3>DE LOS INTERESADOS Y REQUISITOS</h3>
					<br>
					<p class="pJustifi">
					<b>PRIMERA.-</b>La elección de Consejos de Participación Ciudadana y Delegaciones Municipales se realizará mediante la modalidad de Planillas y formulas respectivamente, a celebrarse el próximo día veintisiete  de marzo de dos mil dieciséis, mediante el principio democrático de voto universal, libre, secreto y directo de los ciudadanos.
					<br>
					<br>
					Por cada colonia y/o fraccionamiento y pueblo citado en la presente Convocatoria, existirá una Planilla para el Consejo de Participación Ciudadana, integrado por un Presidente, un Secretario, un Tesorero y dos Vocales, cada uno con su respectivo suplente. Para el caso de las Delegaciones Municipales se integrará una Formula  con un Delegado y un Subdelegado así como sus respectivos suplentes. En donde deberán de participar hombres, mujeres y jóvenes mayores de 18 años.
					<br>
					<br>
					<b>SEGUNDA.-</b>El registro de Planillas y Formulas se realizará a solicitud de los interesados, en los formatos  establecidos por la Secretaría del H. Ayuntamiento.
					<br>
					<br>
					<b>TERCERA.-</b>Podrán participar todos los ciudadanos mexicanos vecinos del Municipio de Nicolás Romero, en pleno goce de sus derechos políticos y civiles, y que cumplan con todos los requisitos de elegibilidad que establece esta convocatoria y demás disposiciones legales aplicables, mismos que podrán ser electos únicamente dentro de la colonia y/o fraccionamiento o pueblo que habiten.
					<br>
					<br>
					Los vecinos que deseen participar en la integración de los Consejos de Participación Ciudadana y Delegaciones municipales, que postulen Planillas y Formulas para la renovación de los mismos deberán de cumplir con los siguientes requisitos:
					<br>
					<br>
					<b>I. </b>Ser ciudadano mexicano en pleno goce y ejercicio de sus derechos civiles y políticos;
					<br>
					<br>
					<b>II. </b>Ser habitante del Municipio de Nicolás Romero y conocer las necesidades de la comunidad que aspire representar;
					<br>
					<br>
					<b>III. </b>Tener una residencia mínima de tres años  en el Municipio y en la localidad que pretenda representar.  
					<br>
					<br>
					<b>IV. </b>Contar con credencial para votar actualizada y vigente, expedida por el Instituto Nacional Electoral, y en donde conste que su domicilio actual pertenece a la comunidad que se pretenda representar;
					<br>
					<br>
					<b>V. </b>Gozar de buena reputación, ser de reconocida probidad;
					<br>
					<br>
					<b>VI. </b>No contar con antecedentes penales, ni encontrarse sujeto a un proceso penal por delitos que merezcan pena privativa de libertad;
					<br>
					<br>
					<b>VII. </b>No haber desempeñado funciones como Presidente, Secretario, Tesorero o Vocal, propietario o suplente del Consejo de Participación Ciudadana o Delegación en el periodo que concluye; 
					<br>
					<br>
					<b>VIII. </b>No ser servidor público dentro de la Administración Pública Municipal, Estatal o Federal, y en caso de fungir como tal, haber sido separado de esté por lo menos seis meses anteriores a su designación; y 
					<br>
					<br>
					<b>IX. </b>No ser miembro directivo de algún partido, agrupación política o ministro de algún culto religioso; 
					El incumplimiento de cualquier requisito señalado en la presente impedirá el registro del aspirante y/o Planilla o Formula.
					</p>
					<br>
					<br>
					<h3>DE LA DOCUMENTACIÓN</h3>
					<br>
					<p class="pJustifi">
					<b>CUARTA.-</b> Para poder ser postulante en la integración de una Planilla o Formula para la renovación del  Consejo de Participación Ciudadana o Delegación, deberán presentar la siguiente documentación de manera individual en original y copia para su respectivo cotejo:
					<br>
					<br>
					I. Formato Solicitud  de Registro por Planilla, Formula y Representante de la misma,  expedido por la Secretaría del  H. Ayuntamiento, mismo que estará a disposición de los postulantes el día de la publicación de la presente convocatoria, en las oficinas de dicha dependencia;
					<br>
					<br>
					II. Formato Individual de Registro  de los integrantes de la Planilla, Formula y Representante de la misma, expedido por la Secretaría del H. Ayuntamiento, mismo que será puesto a disposición de los postulantes el mismo día de la publicación de la presente convocatoria en las oficinas de dicha dependencia;
					<br>
					<br>
					III. Formato para la Designación de un Representante de Planilla y Formula, para efectos de recepción de notificaciones vía Estrados, entrega de documentos y demás actividades propias del proceso, el cual  recibirá la acreditación correspondiente en el formato que para ello expida la Secretaria del H. Ayuntamiento;
					<br>
					<br>
					IV. Copia Certificada del Acta de nacimiento actualizada para cotejo;
					<br>
					<br>
					V. Constancia domiciliaría expedida por la Secretaría del H. Ayuntamiento original y actualizada;
					<br>
					<br>
					VI. Credencial para votar actualizada y vigente, expedida por el Instituto Nacional Electoral;
					<br>
					<br>
					VII. Carta de no antecedentes penales, actualizada;
					VIII. Cuatro fotografías tamaño infantil de frente, en blanco y negro;
					<br>
					<br>
					IX. Presentar por cada Planilla y Formula un Plan de Trabajo que incluya las acciones a implementar durante el periodo de gestión. 
					<br>
					<br>
					X. Presentar la documentación que acredite, el pago de contribuciones municipales (agua potable y predial)
					</p>
					<br>
					<br>
					
					<h3>DEL REGISTRO</h3>
					<p class="pJustifi">
					<b>QUINTA.- </b>El periodo de entrega-recepción de las solicitudes de registro de Planillas y Formulas ante la Secretaría del H. Ayuntamiento, será el correspondiente a los días diecisiete y dieciocho de marzo del presente año, en un horario de 9:00 a 18:00 hrs., en las oficinas que ocupa la Secretaria del Ayuntamiento, ubicadas en el primer piso del Palacio Municipal en Av. Juárez  s/n Col. Centro, Nicolás Romero, Estado de México, C.P. 54400
					En la recepción del Formato de Solicitud de Registro por Planilla, Formula y Representante de la misma, se llevará a cabo la revisión de la documentación presentada, acordando la Secretaría del H. Ayuntamiento la procedencia o improcedencia del registro, siendo este procedente, se extenderá al Representante designado por la Planilla y Formula que se trate, el acuse de recibo correspondiente, en el formato que para ello expida la Secretaria del H. Ayuntamiento.
					<br>
					<br>
					<b>SEXTA.- </b>Las Planillas y Formulas debidamente registradas ante la Secretaría del H. Ayuntamiento, que hayan cumplido con toda la documentación recibirán el Formato de Procedencia de Registro de Planilla el día veintiuno de marzo del presente año, a efecto de que la misma pueda  realizar el proselitismo pertinente y los electores puedan identificar en la Boleta Electoral la opción de su preferencia  al momento de sufragar.
					<br>
					<b>SÉPTIMA.- </b>En el caso de que la documentación recibida no se ajuste a los requisitos señalados en la convocatoria, se notificará inmediatamente al Representante de la Planilla y Formula la improcedencia del registro, para que pueda realizar los ajustes pertinentes, y si así no lo hiciere dentro del término establecido en el periodo de entrega-recepción, no podrá participar en dicho proceso. 
					<br>
					<br>
					</p>
					<h3>
					DE LAS CAMPAÑAS ELECTORALES
					</h3>
					<br>
					<p class="pJustifi">
					<b>OCTAVA.- </b>Las campañas electorales iniciarán el día veintidós de marzo y concluirán el día veinticinco de marzo del presente año y éstas serán el conjunto de actividades llevadas a cabo por los candidatos de la Planilla y Formula registrada ante la Secretaria del H. Ayuntamiento, para la obtención del voto.
					<br>
					<br>
					Serán actos de campaña las reuniones públicas y en general aquéllos en que para promover sus Planillas y Formulas, los integrantes de éstas se congreguen o dirijan personalmente a los vecinos de su localidad, en las que se promueva el voto. 
					<br>
					<br>
					Se considerará propaganda electoral al conjunto de escritos, publicaciones, imágenes, grabaciones, proyecciones y/o expresiones que durante la campaña produzcan y difundan las Planillas y Formulas, con el propósito de presentar ante los vecinos sus propuestas. La realizarán, respetando lo establecido por el Código Electoral del Estado de México en materia de Propaganda Electoral, entendiéndose por esto, la colocación y fijación de materiales.
					<br>
					<br>
					Tanto la propaganda electoral como los actos de campaña, deberán enfocarse en la divulgación de propuestas y planteamientos específicos de su Plan de Trabajo. En ningún caso, dicha propaganda podrá ir en contra de la moral y las buenas costumbres, contener injurias, ni atentar contra la integridad y los derechos de la sociedad o de los integrantes de las demás Planillas y Formulas participantes en el proceso. 
					<br>
					<br>
					Las omisiones o actos que las Planillas y Formulas pudieran hacer en contravención de las Bases relativas a las Campañas Electorales, podrán ser causa de amonestación pública e incluso de pérdida del registro.
					<br>
