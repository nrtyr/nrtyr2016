					</p>
					<div class="cuadroArticulo">
					<li>
						Es una política pública que permite la innovación y simplificación de los procesos administrativos, que a su vez, promueve la eficacia y eficiencia del gobierno y fomenta desarrollo socioeconómico y la competitividad del Municipio. 
					</li>
									
					</div>
					<br>
					<br>

					<p class="pArticulo">
					2.- ¿Cuál es el objetivo de la Mejora Regulatoria en Nicolás Romero?
					</p>

					<div class="cuadroArticulo">
					<li>
						Implementar un sistema integral de gestión regulatoria que beneficie a los contribuyentes en solicitudes y trámites, disminuyendo requisitos y tiempos, así como mejorar los procesos administrativos.
					</li>
					
					</div>
					<br>
					<p class="pArticulo">
					3.- ¿Qué es la Comisión Municipal de Mejora Regulatoria?
					</p>
					<div class="cuadroArticulo">
					<li>
						Es el órgano colegiado de coordinación, consulta, apoyo técnico y construcción de consensos para implementar y conducir un proceso continuo y permanente de Mejora Regulatoria en el Municipio.
					</li>
					</div>

					<br>
					<p class="pArticulo">
					4.- ¿Qué es el Registro Municipal de Trámites y Servicios (REMTyS)?
					</p>
					<div class="cuadroArticulo">
					<li>
						Es una plataforma de acceso público que contiene el catálogo de trámites, servicios, requisitos, plazos y cargas tributarias del Ayuntamiento.
					</li>
					</div>

					<br>
					<p class="pArticulo">
					5.- ¿Cómo se implementa la Mejora Regulatoria en el Municipio de Nicolás Romero?
					</p>
					<div class="cuadroArticulo">
					<li>
						Para lograr el objetivo de la Mejora Regulatoria, debe realizarse una serie de actividades, entre ellas, se encuentra la elaboración del programa anual, en donde se especifican las acciones que ha de realizar cada una de las áreas del Ayuntamiento, para optimizar los procesos administrativos y así reducir requisitos y tiempos de respuesta.
					</li>
					</div>

